def normalize_gpu(gpu):
    if gpu is None:return ''
    g=gpu.strip();l=g.lower()
    if l.startswith('rtx ') or l.startswith('gtx '): return 'NVIDIA GeForce '+g
    if l.startswith('rx '): return 'AMD Radeon '+g
    return g
def normalize_ssd(ssd):
    if ssd is None:return 'Unknown'
    s=ssd.lower()
    if 'nvme' in s or 'm.2' in s or 'pcie' in s:return 'M.2'
    if 'sata' in s:return '2.5" SATA'
    return ssd
def normalize_wifi(v):
    if v is None:return 'Unknown'
    t=v.lower()  
    for n in ['7','6','5','4']:
        if n in t:return n+'.0'
    return 'Unknown'
def normalize_bluetooth(v):
    if v is None:return 'Unknown'
    t=v.lower()
    for ver in ['5.4','5.3','5.2','5.1','5.0','4.2','4.1','4.0']:
        if ver in t:return ver
    return 'Unknown'
